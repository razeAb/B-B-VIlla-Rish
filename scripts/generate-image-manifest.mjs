import fs from "node:fs/promises";
import path from "node:path";

const PROJECT_ROOT = path.resolve(process.cwd());
const IMAGES_DIR = path.join(PROJECT_ROOT, "public", "images");
const OUTPUT_FILE = path.join(PROJECT_ROOT, "src", "imageManifest.js");

const isImage = (name) => /\.(png|jpe?g|webp|gif|svg)$/i.test(name);

const main = async () => {
  const entries = await fs.readdir(IMAGES_DIR, { withFileTypes: true });
  const files = entries
    .filter((entry) => entry.isFile() && isImage(entry.name))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b, "en", { numeric: true, sensitivity: "base" }));

  const manifest = await Promise.all(
    files.map(async (fileName) => {
      const filePath = path.join(IMAGES_DIR, fileName);
      const stat = await fs.stat(filePath);
      return {
        fileName,
        src: `/images/${encodeURIComponent(fileName)}`,
        bytes: stat.size,
      };
    })
  );

  const content = `// This file is auto-generated by scripts/generate-image-manifest.mjs
// Run: npm run images:manifest

const imageManifest = ${JSON.stringify(manifest, null, 2)};

export default imageManifest;
`;

  await fs.writeFile(OUTPUT_FILE, content, "utf8");
  console.log(`Wrote ${manifest.length} images to ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

